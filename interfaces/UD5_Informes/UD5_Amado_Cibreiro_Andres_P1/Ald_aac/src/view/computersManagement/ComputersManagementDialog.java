    public boolean isAnyTextFieldBlank() {
        String[] textFields = {getSerialNumber(), getBrand(),
            getModel(), getAtribute()
        };

        for (String tf : textFields) {
            if (tf.isBlank()) {
                return true;
            }
        }

        return false;
    }

