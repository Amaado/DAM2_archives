    public void setDisable(String element, boolean disable) {
        switch (element) {
            case "remove":
                if (disable) {
                    this.removeButton.setEnabled(false);
                } else {
                    this.removeButton.setEnabled(true);
                }
                break;

            case "edit":
                if (disable) {
                    this.editButton.setEnabled(false);
                } else {
                    this.editButton.setEnabled(true);
                }
                break;

            case "add":
                if (disable) {
                    this.addButton.setEnabled(false);
                } else {
                    this.addButton.setEnabled(true);
                }
                break;
                
            case "table":
                if (disable) {
                    this.computersTable.setEnabled(false);
                } else {
                    this.computersTable.setEnabled(true);
                }
                break;
            
            case "scrollPane":
                if (disable) {
                    this.computersScrollPane.setEnabled(false);
                } else {
                    this.computersScrollPane.setEnabled(true);
                }
                break;
            default:
                System.out.println("Error desactivando un bot√≥n");
                break;
        }
    }

